package Entity;

public class Subject {
    private Integer ID;
    private String name;

    public Subject(Integer ID, String name) {
        this.ID = ID;
        this.name = name;
    }

    public Integer getID() {
        return ID;
    }

    public void setID(Integer ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Subject{" +
                "ID=" + ID +
                ", name='" + name + '\'' +
                '}';
    }
}
